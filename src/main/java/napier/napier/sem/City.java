package napier.napier.sem;

/**
 * Class to represent a city
 */
public class City {

    /**
     * City's ID number
     */
    public int city_id;

    /**
     * City's name
     */
    public String name;

    /**
     * City's country code
     */
    public String countryCode;

    /**
     * City's district
     */
    public String district;

    /**
     * City's population
     */
    public int population;
}
