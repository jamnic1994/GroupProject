package com.napier.sem;

/**
 * Class to represent a countrys language as an object
 */
public class CountryLanguage {

    /**
     * Country's assigned code
     */
    public String countryCode;

    /**
     * Country's language
     */
    public String language;

    /**
     * Is the language the country's official language
     */
    public String isOfficial;

    /**
     * Percentage of the country that speak this language
     */
    public String percentage;

}
