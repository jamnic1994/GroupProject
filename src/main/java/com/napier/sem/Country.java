package com.napier.sem;

/**
 * Class to represent a city
 * A country report requires the following columns: Code, Name, Continent, Region, Population, Capital.
 */
public class Country {

    /**
     * Country code
     */
    public String country_Code;

    /**
     * Country name
     */
    public String country_Name;

    /**
     * Country's continent
     */
    public String country_Continent;

    /**
     * Country's region
     */
    public String country_Region;

    /**
     * Country population
     */
    public int country_Population;

    /**
     * Country capital
     */
    public String country_Capital;
}
