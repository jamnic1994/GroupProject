package com.napier.sem;

/**
 * Class to represent a country
 */
public class Country {

    /**
     * Country's assigned code
     */
    public String Code;

    /**
     * Country's name
     */
    public String name;

    /**
     * Country's continent
     */
    public String continent;

    /**
     * Country's region
     */
    public String region;

    /**
     * Country's surface area
     */
    public float surfaceArea;

    /**
     * Country's year of independence
     */
    public int indepYear;

    /**
     * Country's population
     */
    public int population;

    /**
     * Country's life expectancy
     */
    public double lifeExpectancy;

    /**
     * Country's GNP
     */
    public float gnp;

    /**
     * Country's old GNP
     */
    public float gnpOld;

    /**
     * Country's local name
     */
    public String localName;

    /**
     * Country's form of government
     */
    public String governmentForm;

    /**
     * Country's head of state
     */
    public String headOfState;

    /**
     * Country's capital
     */
    public int capital;

    /**
     * Country's second assigned code
     */
    public String code2;
}
